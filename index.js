function add (a, b) {
  return a + b;
}
function subtract (a,b) {
  return a - b;
}
function multiply (a,b) {
  return a * b;
}
function divide (a,b) {
  return a / b;
}
function increment (n) {
  return ++n;
}
function decrement (n) {
  return --n;
}
function makeInt(string) {
  return parseInt((makeInt(string)), 10);
}
function preserveDecimal(y) {
  return parseFloat(y)
}
